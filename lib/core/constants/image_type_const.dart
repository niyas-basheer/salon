
/// The `ImageType` class is a utility class that defines constants for different
/// types of images.
class ImageType {
  ImageType._();

  /// Represents an image loaded from a network URL.
  static const int network = 1;

  /// Represents an image loaded from an asset.
  static const int asset = 2;

  /// Represents an image loaded from a file.
  static const int file = 3;
}
